package pacoteOperadores;

public class ClasseIncremento2 {

	public static void main(String[] args) {
		int x = 500;
		System.out.printf("Valor de x: %d\n", x);
		x = x - 1;
		System.out.printf("Valor de x: %d\n", x);
		x--;
		System.out.printf("Valor de x: %d\n", x);
		x--;
		System.out.printf("Valor de x: %d\n", x);
		x = x - 2;
		System.out.printf("Valor de x: %d", x);


	}

}
